/** Get all challenges for a chapter */
export function getChallengesForChapter(chapterNumber: number): Challenge[] {
  return Object.values(challengeMap)
    .filter((c) => c.chapter === chapterNumber)
    .sort((a, b) => a.order - b.order);
}
